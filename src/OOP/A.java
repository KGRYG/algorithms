public class A {

   private int x;
   private int y;

    public A(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public A () {}

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
